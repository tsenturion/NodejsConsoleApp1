"use strict";

/* =========================================================
   1) Базовое определение функции + вызов
   ========================================================= */

// Объявляем функцию greet — создаём именованный блок кода.
// Код внутри не выполняется до тех пор, пока функция явно не вызвана.
function greet() {
  console.log("Привет!");
}

// Вызов функции — именно здесь происходит выполнение тела функции.
greet();

/* =========================================================
   2) Параметры функции
   ========================================================= */

// Переопределяем функцию greet, теперь она принимает параметр name.
// name — локальная переменная, доступная только внутри функции.
function greet(name) {
  console.log("Привет, " + name);
}

// Передаём аргументы — конкретные значения для параметра name.
greet("Анна");
greet("Иван");

/* =========================================================
   3) Возврат значения через return
   ========================================================= */

// Функция sum принимает два параметра и возвращает результат сложения.
// После выполнения return функция немедленно завершает работу.
function sum(a, b) {
  return a + b;
}

// Сохраняем возвращаемое значение в переменную.
let result = sum(5, 3);
console.log("Результат sum:", result);

// Используем результат функции прямо внутри выражения.
console.log("10 + 20 =", sum(10, 20));

/* =========================================================
   4) Function Expression
   ========================================================= */

// Создаём функциональное выражение и присваиваем его переменной multiply.
// Переменная multiply хранит ссылку на функцию.
const multiply = function(a, b) {
  return a * b;
};

// Вызываем функцию через имя переменной.
console.log("3 * 4 =", multiply(3, 4));

/* =========================================================
   5) Стрелочные функции (Arrow Functions)
   ========================================================= */

// Стрелочная функция с явным return.
const subtract = (a, b) => {
  return a - b;
};

console.log("10 - 6 =", subtract(10, 6));

// Упрощённый синтаксис стрелочной функции.
// Если тело состоит из одного выражения, return пишется неявно.
const divide = (a, b) => a / b;

console.log("20 / 5 =", divide(20, 5));

/* =========================================================
   6) Значения параметров по умолчанию
   ========================================================= */

// Если аргумент не передан, параметр получает значение по умолчанию.
function greetWithDefault(name = "гость") {
  console.log("Привет, " + name);
}

// Вызов без аргумента — используется дефолтное значение.
greetWithDefault();

// Вызов с аргументом — дефолт игнорируется.
greetWithDefault("Мария");

/* =========================================================
   7) Callback-функции
   ========================================================= */

// Функция processUser принимает имя и другую функцию (callback).
// Внутри она выполняет основную логику и затем вызывает callback.
function processUser(name, callback) {
  console.log("Обрабатываем пользователя:", name);
  callback();
}

// Передаём анонимную функцию как аргумент.
processUser("Анна", function() {
  console.log("Обработка завершена");
});

// Передаём стрелочную функцию как callback.
processUser("Иван", () => {
  console.log("Дополнительная обработка завершена");
});

/* =========================================================
   8) Область видимости
   ========================================================= */

function testScope() {
  let x = 10; // Локальная переменная — существует только внутри функции.
  console.log("Внутри функции x =", x);
}

testScope();

// Попытка доступа к x вне функции вызовет ошибку.
// Чтобы программа не прервалась, демонстрируем это через try/catch.
try {
  console.log(x);
} catch (error) {
  console.log("Ошибка доступа к x вне функции:", error.message);
}

/* =========================================================
   9) Замыкание (Closure)
   ========================================================= */

// Функция createCounter создаёт локальную переменную count.
// Возвращаемая функция сохраняет доступ к этой переменной.
function createCounter() {
  let count = 0;

  return function() {
    count++;
    return count;
  };
}

// Создаём независимый экземпляр счётчика.
const counter = createCounter();

console.log("Counter:", counter()); // 1
console.log("Counter:", counter()); // 2
console.log("Counter:", counter()); // 3

/* =========================================================
   10) Чистая функция
   ========================================================= */

// Чистая функция: результат зависит только от входных параметров,
// и она не изменяет внешнее состояние.
function add(a, b) {
  return a + b;
}

console.log("Чистая функция add(2,3) =", add(2, 3));
console.log("Чистая функция add(2,3) =", add(2, 3)); // одинаковый результат

/* =========================================================
   11) Функции как объекты
   ========================================================= */

// Функции являются объектами, поэтому им можно добавлять свойства.
function sayHello(name) {
  return "Привет, " + name;
}

// Добавляем пользовательское свойство.
sayHello.description = "Функция приветствия пользователя";

console.log(sayHello("Олег"));
console.log("Описание функции:", sayHello.description);

/* =========================================================
   12) Возврат функции из функции
   ========================================================= */

// Функция высшего порядка — возвращает другую функцию.
function createMultiplier(factor) {
  return function(number) {
    return number * factor;
  };
}

const double = createMultiplier(2);
const triple = createMultiplier(3);

console.log("Double 5 =", double(5));
console.log("Triple 5 =", triple(5));