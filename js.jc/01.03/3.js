"use strict";

/* =========================================================
   1) Базовое определение функции + вызов
   ========================================================= */

// Объявляем функцию greet — создаём именованный блок кода.
// Код внутри пока не выполняется, он только сохраняется для дальнейшего вызова.
function greet() {
  console.log("Привет!");
}

// Явно вызываем функцию greet после её объявления.
// Только в этой точке запускается код, который лежит внутри функции.
greet();

/* =========================================================
   2) Параметры функции
   ========================================================= */

// Переопределяем функцию greet, теперь она принимает параметр name.
// name — локальная переменная, доступная только внутри функции.
function greet(name) {
  console.log("Привет, " + name);
}

// Передаём аргументы — конкретные значения для параметра name.
// Каждый вызов использует своё значение и формирует отдельное приветствие.
greet("Анна");
greet("Иван");

/* =========================================================
   3) Возврат значения через return
   ========================================================= */

// Функция sum принимает два параметра и возвращает результат сложения.
// После выполнения return функция немедленно завершает работу.
function sum(a, b) {
  return a + b;
}

// Сохраняем возвращаемое значение в переменную result.
// После этого можем использовать результат отдельно от момента вычисления.
let result = sum(5, 3);
console.log("Результат sum:", result);

// Здесь не сохраняем ответ в переменную, а выводим его сразу.
// Такой стиль удобен, когда промежуточное значение больше не понадобится.
console.log("10 + 20 =", sum(10, 20));

/* =========================================================
   4) Function Expression
   ========================================================= */

// Показываем, что функциональное выражение нельзя вызвать до инициализации.
// Пример оставлен закомментированным, чтобы скрипт продолжал выполняться.
// sayHello();
// ReferenceError: sayHello is not defined

// Функциональное выражение сохраняем в переменную, чтобы вызывать его позже.
// В отличие от function declaration, здесь важен порядок объявления.
const sayHello = function() {
    console.log("Привет из функционального выражения!");
};

// Вызываем функцию через переменную, где хранится ссылка на неё.
// Это стандартный способ работы с function expression.
sayHello();

// Именованное функциональное выражение: имя fact доступно для рекурсии внутри самой функции.
// Снаружи остаётся доступной только переменная factorial.
const factorial = function fact(n) {
    if (n <= 1) {
        return 1;
    }
    return n * fact(n - 1);
};

// Проверяем корректный вызов через переменную factorial.
// Для n = 5 ожидаем результат 120 при корректной рекурсии.
try {
    console.log("Факториал 5:", factorial(5));
} catch (error) {
    console.log("Ошибка:", error.message);
}

// Снаружи имя fact недоступно, поэтому получаем ошибку области видимости.
// Это демонстрирует разницу между внутренним именем и внешней переменной.
try {
    console.log("Факториал 5:", fact(5));
} catch (error) {
    console.log("Ошибка:", error.message);
}


// Функция высшего порядка: принимает функцию и выполняет её.
// Такой подход позволяет передавать поведение как аргумент.
function run(fn) {
    fn();
}

// Передаём анонимную функцию как аргумент в run.
// run вызовет её внутри себя, и сообщение появится в консоли.
run(function() {
    console.log("Анонимная функция в качестве аргумента");
});




// Объявляем переменную, в которую запишем одну из функций в зависимости от условия.
// Позже вызовем её единообразно, не зная заранее конкретную реализацию.
let action;

// Выбираем нужное поведение в зависимости от флага asAdmin.
// В обеих ветках присваиваем функцию с разной бизнес-логикой.
if (asAdmin) {
    action = function() {
        console.log("Действия для администратора");
    };
} else {
    action = function() {
        console.log("Действия для обычного пользователя");
    };
}

// Вызываем функцию, которая была выбрана в блоке if/else.
// За счёт этого интерфейс вызова остаётся одинаковым для обеих ролей.
action();


// Функциональное выражение с локальной переменной внутри тела функции.
// Переменная secret будет существовать только во время выполнения calculate.
const calculate = function() {
    // Это локальное значение недоступно за пределами функции calculate.
    // Снаружи к нему нельзя обратиться напрямую по имени secret.
    const secret = 42;
    return secret * 2;
};

// Доступ к secret происходит только через функцию calculate.
// Внешний код получает уже вычисленный результат, а не саму переменную.
try {
    console.log("Секретное число:", calculate());
} catch (error) {
    console.log("Ошибка:", error.message);
}

// Прямой доступ к secret вне функции вызывает ошибку.
// Ниже демонстрируем это безопасно через try/catch.
try {
    console.log("Секретное число:", secret * 2);
} catch (error) {
    console.log("Ошибка:", error.message);
}


// Короткая стрелочная функция с неявным return.
// Результат выражения a + b автоматически становится возвращаемым значением.
const sum = (a, b) => a + b;




// Возвращаем функцию-логгер, которая печатает переданное сообщение.
// Это пример функции, которая создаёт и отдаёт другую функцию.
function createLogger() {
    return function(message) {
        console.log("Лог:", message);
    };
}

// Создаём логгер и сразу используем его.
// Теперь logger можно вызывать много раз с разными сообщениями.
const logger = createLogger();
logger("Это сообщение будет залогировано");



// Обычное объявление функции для суммирования двух чисел.
// Такая запись поднимается (hoisting) и доступна во всей текущей области.
function calculateSum(a, b) {
    return a + b;
}




// Принимаем имя пользователя и callback, который вызывается после приветствия.
// Сначала выполняется основная логика, затем переданная функция продолжения.
function greetUser(name, callback) {
    console.log("Привет, " + name);
    callback();
}

// Именованный callback для прощания.
// Его можно передавать в другие функции как готовое поведение.
function sayGoodbye() {
    console.log("Пока!");
}

// Передаём именованный callback.
// В этом случае greetUser вызовет функцию sayGoodbye после приветствия.
greetUser("Мария", sayGoodbye);

// Передаём анонимный callback прямо в вызов функции.
// Такой вариант удобен, когда обработчик нужен только в одном месте.
greetUser("Пётр", function() {
    console.log("До свидания!");
});

// setTimeout вызывает callback после задержки в 2000 мс.
// Это демонстрирует отложенное выполнение функции в JavaScript.
setTimeout(function() {
    console.log("Это сообщение появится через 2 секунды");
}, 2000);


// Формируем строку и передаём её в callback как аргумент.
// Так вызывающий код получает уже подготовленное сообщение.
function processUser(name, callback) {
    const message = "Пользователь: " + name;
    callback(message);
}

// Получаем сообщение в параметр msg и завершаем обработку.
// Это пример передачи данных через callback-функцию.
processUser("Сергей", function(msg) {
    console.log("Обработка завершена. " + msg);
});


// Пример глубокой вложенности callback-ов (callback hell).
// С ростом вложенности такой код становится сложнее читать и поддерживать.
doSomething(function(result1) {
    doSomethingElse(result1, function(result2) {
        doThirdThing(result2, function(result3) {
            console.log("Результат третьей операции:", result3);
        });
    });
});


// Имитация асинхронной операции с двумя callback: успех и ошибка.
// Один обработчик вызывается при успехе, второй — при неудаче.
function asyncOperation(successCallback, errorCallback) {
    // Случайным образом выбираем, считать операцию успешной или нет.
    // Это нужно только для демонстрации двух веток обработки.
    let ok = Math.random() > 0.5;
    if (ok) successCallback("Операция прошла успешно");
    else errorCallback("Произошла ошибка");
}

// Передаём обработчики результата через стрелочные функции.
// Первый обработчик печатает успех, второй выводит сообщение об ошибке.
asyncOperation(
    result => console.log(result),
    error => console.error(error)
);


// Создаём функциональное выражение и присваиваем его переменной multiply.
// Переменная multiply хранит ссылку на функцию.
const multiply = function(a, b) {
  return a * b;
};

// Вызываем функцию через имя переменной.
// В консоль выводится результат умножения двух чисел.
console.log("3 * 4 =", multiply(3, 4));

/* =========================================================
   5) Стрелочные функции (Arrow Functions)
   ========================================================= */

// Стрелочная функция с явным return.
// Фигурные скобки требуют явного возврата через ключевое слово return.
const subtract = (a, b) => {
  return a - b;
};

// Проверяем работу функции subtract.
// Здесь ожидаем разницу между первым и вторым аргументами.
console.log("10 - 6 =", subtract(10, 6));

// Упрощённый синтаксис стрелочной функции.
// Если тело состоит из одного выражения, return пишется неявно.
const divide = (a, b) => a / b;

// Проверяем работу функции divide.
// Результат деления выводим сразу, без промежуточной переменной.
console.log("20 / 5 =", divide(20, 5));

// Если параметр один, скобки вокруг него можно не писать.
// Функция square возвращает квадрат переданного числа.
const square = x => x * x;

// Если параметров нет, нужно указать пустые круглые скобки.
// Эта функция просто печатает фиксированное приветствие.
const greet = () => console.log("Привет из стрелочной функции!");

/* =========================================================
   6) Значения параметров по умолчанию
   ========================================================= */

// Если аргумент не передан, параметр получает значение по умолчанию.
