/* =========================================================
   9) Замыкание (Closure)
   ========================================================= */

// Функция createCounter создаёт локальную переменную count.
// Возвращаемая функция сохраняет доступ к этой переменной.
function createCounter() {
  let count = 0;

  return function() {
    count++;
    return count;
  };
}

// Создаём независимый экземпляр счётчика.
// Переменная count сохраняется между вызовами за счёт замыкания.
const counter = createCounter();

// Каждый вызов увеличивает внутреннее состояние замыкания.
// Поэтому подряд получаем возрастающие значения: 1, затем 2, затем 3.
console.log("Counter:", counter());
console.log("Counter:", counter());
console.log("Counter:", counter());

/* =========================================================
   10) Чистая функция
   ========================================================= */

// Чистая функция: результат зависит только от входных параметров,
// и она не изменяет внешнее состояние.
function add(a, b) {
  return a + b;
}

// Одинаковые аргументы дают одинаковый результат.
// Повторные вызовы не зависят от внешнего состояния программы.
console.log("Чистая функция add(2,3) =", add(2, 3));
console.log("Чистая функция add(2,3) =", add(2, 3));

/* =========================================================
   11) Функции как объекты
   ========================================================= */

// Функции являются объектами, поэтому им можно добавлять свойства.
// Ниже объявляем функцию и затем расширяем её пользовательским полем.
function sayHello(name) {
  return "Привет, " + name;
}

// Добавляем пользовательское свойство.
// Это свойство хранит дополнительное описание поведения функции.
sayHello.description = "Функция приветствия пользователя";

// Выводим результат вызова и дополнительное свойство функции.
// Так видно, что функция одновременно вызывается и содержит данные.
console.log(sayHello("Олег"));
console.log("Описание функции:", sayHello.description);

/* =========================================================
   12) Возврат функции из функции
   ========================================================= */

// Функция высшего порядка — возвращает другую функцию.
// Внутренняя функция запоминает factor из внешней области видимости.
function createMultiplier(factor) {
  return function(number) {
    return number * factor;
  };
}

// Создаём две функции с разными коэффициентами.
// double умножает на 2, а triple умножает на 3.
const double = createMultiplier(2);
const triple = createMultiplier(3);

// Проверяем результат для каждой созданной функции.
// Один и тот же аргумент даёт разные результаты из-за разных factor.
console.log("Double 5 =", double(5));
console.log("Triple 5 =", triple(5));
