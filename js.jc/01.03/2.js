// =========================
// Условные операторы
// =========================

// Пример переменной для демонстрации условий
let age = 20;

// if — выполняет блок кода только если условие истинно
if (age >= 18) {
  console.log("Доступ разрешён");
}

// if / else — ветвление на два сценария
if (age >= 18) {
  console.log("Доступ разрешён");
} else {
  console.log("Доступ запрещён");
}

// else if — цепочка проверок сверху вниз
let score = 75;

if (score >= 90) {
  console.log("Отлично");
} else if (score >= 70) {
  console.log("Хорошо");
} else {
  console.log("Нужно подтянуть знания");
}

// =========================
// Конструкция switch
// =========================

let day = "Friday";

// switch сравнивает одно значение с несколькими вариантами
switch (day) {
  case "Monday":
    console.log("Начало недели");
    break; // предотвращает «проваливание» в следующий case
  case "Friday":
    console.log("Почти выходные");
    break;
  default:
    console.log("Обычный день");
}

// =========================
// Цикл for
// =========================

// Используется, когда заранее известно количество повторений\
/*
for (инициализация; условие; шаг) {
 тело цикла
}
*/


for (let i = 0, j = 10; i < j; i += 2, j--) {
  console.log(i, j);
}

for (let i = 0; i < 5; i++) {
    for (let j = 0; j < 3; j++) {
        console.log(`i: ${i}, j: ${j}`);
    }
}

// =========================
// Цикл while
// =========================

let count = 0;

// Выполняется, пока условие истинно
while (count < 5) {
  console.log("while:", count);
  count++; // изменение счётчика, иначе будет бесконечный цикл
}

// =========================
// Цикл do...while
// =========================

let number = 0;

// Тело цикла выполняется минимум один раз
do {
  console.log("do...while:", number);
  number++;
} while (number < 3);

// =========================
// break и continue
// =========================

// break — полностью прерывает цикл
for (let i = 0; i < 10; i++) {
  if (i === 5) {
    break; // выход из цикла при достижении 5
  }
  console.log("break example:", i);
}

// continue — пропускает текущую итерацию
for (let i = 0; i < 5; i++) {
  if (i === 2) {
    continue; // пропускаем значение 2
  }
  console.log("continue example:", i);
}

// =========================
