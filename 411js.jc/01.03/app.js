/*
  Сокращённая форма $(function () { ... }) означает:
  выполнить код только после полной готовности DOM.
*/
$(function () {
  // Выводим в консоль ссылку на функцию jQuery для проверки подключения.
  console.log("Проверка jQuery:", $);

  // Сразу после загрузки DOM меняем текст статуса.
  $("#status").text("DOM загружен, jQuery подключена, код app.js выполняется.");

  /*
    Обработчик клика по кнопке.
    Когда пользователь нажмёт кнопку, выполнится функция ниже.
  */
  $("#actionButton").on("click", function () {
    // Меняем заголовок страницы.
    $("#title").text("Кнопка нажата");

    // Обновляем текст статуса.
    $("#status").text("Событие click обработано через jQuery.");

    // Меняем саму кнопку.
    $(this).text("Нажатие обработано");

    /*
      Метод .attr() меняет HTML-атрибут.
      Здесь мы добавляем подсказку title,
      которая появляется при наведении мыши.
    */
    $(this).attr("title", "Эта кнопка уже была нажата");

    /*
      Метод .prop() меняет DOM-свойство элемента.
      Здесь мы отключаем кнопку после первого нажатия.
    */
    $(this).prop("disabled", true);
  });
});