/*
  Сокращённая запись $(function () { ... }) означает:
  выполнить код только после полной готовности DOM.

  Это нужно для того, чтобы все элементы страницы уже существовали,
  и jQuery могла их найти по селекторам.
*/
$(function () {
  // Проверяем в консоли, что функция $ существует и библиотека подключена.
  console.log("jQuery загружена:", $);

  /*
    Сохраняем jQuery-объект в переменную.
    Здесь переменная titleElement хранит не обычный DOM-узел,
    а именно jQuery-объект с методами библиотеки.
  */
  const titleElement = $("#title");

  /*
    Обработчик клика по кнопке изменения заголовка.
    Когда пользователь нажимает кнопку, выполняется переданная функция.
  */
  $("#changeTitleButton").on("click", function () {
    /*
      Метод .text() меняет текст внутри выбранного элемента.
      Мы обращаемся к уже сохранённому jQuery-объекту titleElement.
    */
    titleElement.text("Заголовок изменён через jQuery");

    /*
      Метод .text() применяется к блоку вывода сообщения.
      Так пользователь видит результат не только визуально,
      но и в отдельной области состояния.
    */
    $("#output").text("Кнопка «Изменить заголовок» нажата.");
  });

  /*
    Обработчик клика по кнопке активации блока.
    Здесь показана цепочка методов jQuery.
  */
  $("#activateBoxButton").on("click", function () {
    $("#box")
      // Добавляем CSS-класс active выбранному элементу.
      .addClass("active")
      // Меняем цвет текста через jQuery.
      .css("color", "darkred")
      // Меняем текст блока.
      .text("Активный блок");

    $("#output").text("Блок активирован. Сработала цепочка методов jQuery.");
  });

  /*
    Обработчик клика по кнопке выделения всех элементов списка.
    Селектор ".item" находит все элементы с классом item.
    jQuery автоматически применяет методы ко всем найденным элементам.
  */
  $("#markItemsButton").on("click", function () {
    $(".item")
      // Добавляем класс каждому найденному элементу.
      .addClass("list-item")
      // Меняем текст сразу у всех элементов списка.
      .text("Элемент списка обработан");

    $("#output").text("Все элементы с классом .item были изменены сразу.");
  });

  /*
    Обработчик кнопки сброса.
    Возвращаем странице исходное состояние.
  */
  $("#resetButton").on("click", function () {
    // Возвращаем исходный текст заголовка.
    $("#title").text("Изучаем синтаксис jQuery");

    // Возвращаем исходное состояние блока.
    $("#box")
      .removeClass("active")
      .css("color", "")
      .text("Обычный блок");

    /*
      Для каждого элемента списка возвращаем исходный текст.
      Метод .each() перебирает найденные элементы по одному.
    */
    $(".item").each(function (index) {
      // Удаляем класс выделения у текущего элемента.
      $(this).removeClass("list-item");

      // Возвращаем текст в зависимости от номера элемента.
      $(this).text("Пункт " + (index + 1));
    });

    $("#output").text("Все изменения сброшены.");
  });
});