"use strict";

/* =========================================================
   1) Создание массивов
   ========================================================= */

// Создаём массив через литерал, это самый простой и наглядный вариант.
// Элементы сразу перечисляем в квадратных скобках в нужном порядке.
const numbers = [1, 2, 3, 4, 5];

// Выводим массив numbers, чтобы увидеть его текущее содержимое.
// Это помогает убедиться, что элементы записались корректно.
console.log("numbers:", numbers);

// Создаём массив через конструктор Array с теми же значениями.
// Такой стиль встречается реже, но важно понимать разницу синтаксиса.
const numbers2 = new Array(1, 2, 3, 4, 5);

// Печатаем второй массив для сравнения с литеральным вариантом.
// В данном случае результат будет таким же по содержимому.
console.log("numbers2:", numbers2);

// Если передать в Array один числовой аргумент, это будет длина массива.
// Элементы не создаются явно, внутри появляются пустые слоты.
const emptySlots = new Array(5);

// Показываем массив с пустыми слотами и его длину.
// Так наглядно видно отличие от массива с реальными значениями.
console.log("new Array(5):", emptySlots, "length =", emptySlots.length);

// Создаём пустой массив, в который будем добавлять данные вручную.
// Элементы можно записывать напрямую по конкретному индексу.
const arr = [];

// Записываем значение в нулевой индекс массива arr.
// Если индекса ещё не было, он будет создан автоматически.
arr[0] = "Первый";

// Записываем значение в индекс 1, продолжая заполнять массив.
// После этого в массиве будет уже два элемента по порядку.
arr[1] = "Второй";

// Выводим итоговое состояние массива после ручного заполнения.
// Это подтверждает, что значения поставлены по нужным индексам.
console.log("arr:", arr);

/* =========================================================
   2) Доступ к элементам и свойство length
   ========================================================= */

// Создаём массив строк для примеров чтения по индексу и длине.
// Каждый элемент хранится на позиции с номером, начиная с нуля.
const fruits = ["яблоко", "банан", "груша"];

// Читаем первый элемент массива по индексу 0.
// В массивах JavaScript нумерация всегда начинается с нуля.
console.log("fruits[0]:", fruits[0]);

// Читаем элемент по индексу 2, это третий элемент массива.
// Такой доступ возвращает значение, если индекс существует.
console.log("fruits[2]:", fruits[2]);

// Обращаемся к несуществующему индексу 10 в коротком массиве.
// В этом случае JavaScript возвращает undefined, а не ошибку.
console.log("fruits[10]:", fruits[10]);

// Свойство length показывает текущую длину массива.
// Значение length равно максимальному индексу плюс один.
console.log("length:", fruits.length);

// Записываем значение в индекс 5, пропуская несколько позиций.
// Массив расширится, а между элементами появятся пустые слоты.
fruits[5] = "манго";

// Выводим массив после записи в дальний индекс.
// Так видно, что длина выросла и структура изменилась.
console.log("После fruits[5] = 'манго':", fruits);

// Дополнительно печатаем новую длину после расширения.
// Это показывает связь между индексами и свойством length.
console.log("Новая длина:", fruits.length);

/* =========================================================
   3) Итерация по массивам
   ========================================================= */

// Цикл for даёт полный контроль над индексом и условиями обхода.
// Этот вариант полезен, когда нужен доступ и к индексу, и к значению.
console.log("Цикл for:");

// Идём по индексам от 0 до fruits.length - 1.
// На каждой итерации выводим и индекс, и соответствующий элемент.
for (let i = 0; i < fruits.length; i++) {
  console.log("index:", i, "value:", fruits[i]);
}

// Цикл for...of последовательно возвращает только значения элементов.
// Он короче и удобен, когда индексы в коде не требуются.
console.log("Цикл for...of:");

// Перебираем каждый элемент массива fruits по одному.
// В переменной fruit на каждой итерации лежит текущее значение.
for (const fruit of fruits) {
  console.log("value:", fruit);
}

// Метод forEach вызывает функцию для каждого элемента массива.
// В callback можно получить и текущее значение, и индекс.
console.log("Метод forEach:");

// Передаём callback, который выполнится для каждого элемента fruits.
// Внутри callback печатаем индекс элемента и его содержимое.
fruits.forEach((fruit, index) => {
  console.log("index:", index, "value:", fruit);
});

/* =========================================================
   4) Методы добавления и удаления (мутируют массив)
   ========================================================= */

// Создаём массив, который будем изменять методами добавления и удаления.
// Эти методы мутируют исходный массив прямо на месте.
const modifiableFruits = ["яблоко", "банан"];

// Метод push добавляет новый элемент в конец массива.
// После вызова длина массива увеличивается на один.
modifiableFruits.push("апельсин");

// Выводим массив после push, чтобы увидеть новое состояние.
// Элемент "апельсин" должен оказаться в самом конце.
console.log("После push:", modifiableFruits);

// Метод pop удаляет последний элемент и возвращает его.
// Сохраняем удалённое значение в переменную last.
const last = modifiableFruits.pop();

// Печатаем удалённый элемент, который вернул метод pop.
// Это удобно, если удалённое значение ещё нужно в коде.
console.log("Удалён последний:", last);

// Показываем массив после удаления последнего элемента.
// Видно, что длина массива уменьшилась на один.
console.log("После pop:", modifiableFruits);

// Метод unshift добавляет элемент в начало массива.
// Все существующие элементы при этом сдвигаются вправо.
modifiableFruits.unshift("киви");

// Выводим массив после unshift для проверки результата.
// Новый элемент должен стоять на позиции с индексом 0.
console.log("После unshift:", modifiableFruits);

// Метод shift удаляет первый элемент и возвращает его.
// Сохраняем значение в first для дальнейшего использования.
const first = modifiableFruits.shift();

// Выводим удалённый первый элемент после shift.
// Это подтверждает, какое значение было снято с начала.
console.log("Удалён первый:", first);

// Показываем массив после удаления первого элемента.
// Остальные элементы смещаются влево на одну позицию.
console.log("После shift:", modifiableFruits);

/* =========================================================
   5) Поиск и проверка элементов
   ========================================================= */

// Создаём массив для демонстрации методов поиска.
// Значение "банан" повторяется, чтобы увидеть разницу индексов.
const searchFruits = ["яблоко", "банан", "груша", "банан"];

// indexOf возвращает индекс первого найденного совпадения.
// Если элемент не найден, метод возвращает -1.
console.log("indexOf('банан'):", searchFruits.indexOf("банан"));

// includes проверяет сам факт наличия элемента в массиве.
// Результат этого метода всегда true или false.
console.log("includes('груша'):", searchFruits.includes("груша"));

// lastIndexOf ищет элемент с конца и даёт последний индекс.
// Это полезно, когда в массиве есть повторяющиеся значения.
console.log("lastIndexOf('банан'):", searchFruits.lastIndexOf("банан"));

/* =========================================================
   6) Преобразование и фильтрация
   ========================================================= */

// Базовый массив чисел для примеров map, filter и reduce.
// От него будем получать новые значения без ручных циклов.
const baseNumbers = [1, 2, 3];

// map применяет функцию к каждому элементу и собирает новый массив.
// Исходный baseNumbers при этом остаётся без изменений.
const doubled = baseNumbers.map(n => n * 2);

// Выводим результат map, где каждое число умножено на два.
// Порядок элементов сохраняется таким же, как в исходном массиве.
console.log("map (doubled):", doubled);

// filter оставляет только элементы, для которых условие истинно.
// Здесь отбираем только чётные числа из baseNumbers.
const even = baseNumbers.filter(n => n % 2 === 0);

// Печатаем отфильтрованный массив с чётными значениями.
// Если условие не подходит, элемент в результат не попадёт.
console.log("filter (even):", even);

// reduce сворачивает массив в одно итоговое значение.
// Аккумулятор acc накапливает сумму начиная с начального 0.
const sum = baseNumbers.reduce((acc, n) => acc + n, 0);

// Выводим итоговую сумму всех элементов массива.
// Это типичный пример использования reduce для агрегирования.
console.log("reduce (sum):", sum);

// Создаём массив букв для стандартной строковой сортировки.
// sort без compare-функции сравнивает элементы как строки.
const letters = ["b", "a", "c"];

// Сортируем буквы по возрастанию кодов Unicode.
// В результате получаем порядок a, b, c.
letters.sort();

// Выводим массив после сортировки на месте.
// Метод sort мутирует исходный массив letters.
console.log("letters sorted:", letters);

// Создаём массив чисел для демонстрации числовой сортировки.
// Без compare-функции числа сортировались бы как строки.
const nums = [10, 2, 30];

// Передаём compare-функцию a - b для правильного порядка чисел.
// Тогда значения будут отсортированы как 2, 10, 30.
nums.sort((a, b) => a - b);

// Выводим итоговый массив после числовой сортировки.
// Это подтверждает корректную работу compare-функции.
console.log("nums sorted:", nums);

/* =========================================================
   7) Соединение и разделение
   ========================================================= */

// Создаём два массива, которые будем объединять.
// Это простой пример работы с concat.
const arr1 = [1, 2];
const arr2 = [3, 4];

// Метод concat возвращает новый массив из двух исходных.
// Исходные arr1 и arr2 остаются без изменений.
const combined = arr1.concat(arr2);

// Выводим результат объединения двух массивов.
// В combined элементы идут в порядке arr1, затем arr2.
console.log("concat:", combined);

// join объединяет элементы массива в одну строку через разделитель.
// Здесь используем запятую и пробел между значениями.
console.log("join:", searchFruits.join(", "));

// Создаём строку со значениями, разделёнными запятыми.
// Далее преобразуем её обратно в массив.
const text = "яблоко,банан,груша";

// split разбивает строку по заданному разделителю.
// В результате получаем массив отдельных слов.
const splitted = text.split(",");

// Печатаем массив после разбиения строки методом split.
// Это обратная операция по отношению к join.
console.log("split:", splitted);

/* =========================================================
   8) Копирование массивов
   ========================================================= */

// Исходный массив, от которого будем делать копии.
// Далее покажем способы копирования без ручного цикла.
const original = ["a", "b", "c"];

// slice создаёт новый массив из части исходного диапазона.
// Берём элементы с индекса 1 до 3, не включая правую границу.
const sub = original.slice(1, 3);

// Выводим частичную копию, полученную методом slice.
// Исходный original при этом не изменяется.
console.log("slice:", sub);

// Spread-оператор создаёт поверхностную копию всего массива.
// Это удобный и современный способ клонирования.
const copy = [...original];

// Печатаем новую копию для визуальной проверки.
// На старте copy содержит те же элементы, что и original.
console.log("spread copy:", copy);

// Добавляем новый элемент только в копию массива.
// Так показываем, что original и copy независимы.
copy.push("d");

// Исходный массив остаётся без добавленного элемента.
// Это подтверждает, что копия действительно отдельная.
console.log("original:", original);

// Копия содержит новый элемент "d" после push.
// Изменения затрагивают только массив copy.
console.log("copy after push:", copy);

/* =========================================================
   9) Массивы и функции высшего порядка
   ========================================================= */

// Массив чисел для цепочки функциональных преобразований.
// На нём покажем map, filter и reduce вместе.
const numbersSet = [1, 2, 3, 4, 5];

// Через map создаём массив квадратов всех чисел.
// Каждый элемент умножается сам на себя.
const squares = numbersSet.map(n => n * n);

// Через filter оставляем только чётные значения.
// Условие n % 2 === 0 отбрасывает нечётные числа.
const evenNumbers = numbersSet.filter(n => n % 2 === 0);

// Через reduce считаем сумму всех элементов массива.
// Начальное значение аккумулятора задаём равным нулю.
const total = numbersSet.reduce((acc, n) => acc + n, 0);

// Выводим массив квадратов после преобразования map.
// Это новый массив, исходный numbersSet не изменён.
console.log("squares:", squares);

// Выводим только чётные элементы после filter.
// Порядок элементов сохраняется исходным.
console.log("evenNumbers:", evenNumbers);

// Выводим итоговую сумму после reduce.
// Это конечное скалярное значение вместо массива.
console.log("total:", total);

/* =========================================================
   10) Проверка мутации vs немутации
   ========================================================= */

// Базовый массив для сравнения мутирующих и немутирующих методов.
// Сначала применим map, затем push к одному и тому же массиву.
const base = [1, 2, 3];

// Метод map создаёт новый массив и не меняет исходный base.
// В mapped попадут значения, умноженные на десять.
const mapped = base.map(n => n * 10);

// Проверяем, что base остался прежним после map.
// Это пример немутирующего метода массива.
console.log("base после map:", base);

// Отдельно выводим новый массив mapped с преобразованными данными.
// Так видно различие между исходным и результатом map.
console.log("mapped:", mapped);

// Метод push изменяет исходный массив, добавляя элемент в конец.
// Это пример мутирующего поведения.
base.push(4);

// Печатаем base после push, чтобы увидеть изменение на месте.
// Теперь в массиве появился новый элемент со значением 4.
console.log("base после push:", base);
